/*L'incrementation est l'opération qui consiste à ajouter 1 (et par extention toute valeur entiere fixée) à un compteur. L'opération inverse est la decrementation. Elle consiste à retirer 1 au compteur.*/
/* L'opérateur d'incrementation ajoute une unité a son opérande et renvoie une valeur.
/* ------INCREMENTATION-------*/
var nb1= 1;
nb1 = nb1 + 1; // j'augmente nb1 de 1
console.log(nb1);// affichage => nb1 =2
/*-------ECRITURE SIMPLIFIEE--------*/
nb1++
console.log(nb1);
/* affichage => nb1 =3
/*-------ECRITURE SIMPLIFIEE--------*/
++nb1;
console.log(nb1);// autre façon d'ecriture simplifiée, a vant ou apres le ++ donne ici "+1"

/*-------DECREMENTATION------------*/
nb1 = nb1-1;
console.log(nb1);
/* -----ECRITURE SIMPLIFIEE--------*/
nb1--;
console.log(nb1);// affichage nb1 = 2